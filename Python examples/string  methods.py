name = "Joe"

print(len(name))
# gives length of str index starts with 0
print(name.find("o"))
# gives first index of character in str
print(name.capitalize())
# capitalizes first character in str
print(name.upper())
# upper cases all text
print(name.lower())
# lower cases all text
print(name.isdigit())
# gives boolean value if digit
print(name.isalpha())
# gives boolean value if alphabetical
print(name.count("o"))
# count num of () appears in str
print(name.replace("o","a"))
# replaces character "o" with "a"
print(name*3)
# multiples number of times printed



































































